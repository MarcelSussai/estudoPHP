<?php
require_once 'Pessoa.php';
// ────────────────────────────────────────────────────────────────────────────────────────────────┐
//  Professor
    class Professor extends Pessoa {
  // ┌─────────────────────────────────────────────────────────────────────────────────────────────┐
  //    Atributos
        private $especialidade;
        private $salario;
  // └─────────────────────────────────────────────────────────────────────────────────────────────┘

  // ┌─────────────────────────────────────────────────────────────────────────────────────────────┐
  //    Getters e Setters
  // |─────────────────────────────────────────────────────────────────────────────────────────────|
  //    Set especialidade
        public function setEspecialidade($v) {
            $this->especialidade = $v;
        }
  //    Get especialidade
        public function getEspecialidade() {
            return $this->especialidade;
        }
  // |─────────────────────────────────────────────────────────────────────────────────────────────|
  //    Set salario
        public function setSalario($v) {
            $this->salario = $v;
        }
  //    Get salario
        public function getSalario() {
            return $this->salario;
        }
  // └─────────────────────────────────────────────────────────────────────────────────────────────┘

  // ┌─────────────────────────────────────────────────────────────────────────────────────────────┐
  //    Construtor
        public function __construct($esp, $sal) {
            $this->setEspecialidade($esp);
            $this->setSalario($sal);
        }
  // └─────────────────────────────────────────────────────────────────────────────────────────────┘

  // ┌─────────────────────────────────────────────────────────────────────────────────────────────┐
  //    Métodos Próprios
  // |─────────────────────────────────────────────────────────────────────────────────────────────|
  //    receberAum()
        public function receberAum($a){
            $this->setSalario($this->getSalario() + $a);
        }

  // └─────────────────────────────────────────────────────────────────────────────────────────────┘

    }
// ────────────────────────────────────────────────────────────────────────────────────────────────┘